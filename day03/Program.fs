let inline modulo n m =
    let md = n % m
    if sign md >= 0 then md
    else abs m + md

let down2 data =
    data
    |> Array.mapi (fun i l -> if modulo i 2 = 0 then l else " ")
    |> Array.filter ((<>) " ")

let rec search (data: string list) add pos acc =
    match data with
    | [] -> acc
    | x :: xs ->
        let ind = (modulo (pos + add) x.Length)
        if x.[ind] = '#' then
            search xs add (pos + add) (acc + 1)
        else
            search xs add (pos + add) acc

